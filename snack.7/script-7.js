/* CICLO FOR DA 1 A 1000 INCREMENTO DI 1 */
for (let i = 1 ; i <= 1000; i++){

    /* CREO UNA VARIBILE E GLI ASSEGNO UN METODO POW IN MODO CHE MI ELVA ALLA SECONDA I NUMERI DELLA I */
    let power = Math.pow(2, i);

    /* CONTROLLO RISULTATO */
    console.log(power);
}